package ex1;

public class Calcul {

	/**
	 * Addictionne le premier et second paramètre
	 * @param nb1 Premier nombre à additionner
	 * @param nb2 Second nombre à additionner
	 * @return Renvoie l'addition entre le premier et second paramètre
	 */
	public int addition(int nb1, int nb2){
		return nb1 + nb2;
	}
	
	/**
	 * Soustrait le second paramètre au premier
	 * @param nb1 Nombre auquel on soustrait
	 * @param nb2 Nombre qui soustrait
	 * @return Renvoie la soustraction entre le premier et second paramètre
	 */
	public int soustraction(int nb1, int nb2){
		return nb1 - nb2;
	}
	
}
